def _conn_block(binary: str, host: str, port: str) -> str:
    """
    Generate the ``io`` connection setup block.

    If *host* and *port* are provided the template uses
    ``remote()`` for the remote connection and ``process()`` for local testing,
    selectable via a ``LOCAL`` flag.  Otherwise it always uses ``process()``.

    Args:
        binary: Path to the binary (used as the ``process()`` argument).
        host:   Remote hostname (empty = always local).
        port:   Remote port string (empty = always local).

    Returns:
        A multi-line Python string for the connection setup block.
    """
    if host and port:
        return f'''\
# Toggle LOCAL to switch between local testing and the remote target.
LOCAL = False

if LOCAL:
    io = process("{binary}")
    # Uncomment to attach GDB automatically when testing locally:
    # gdb.attach(io, gdbscript="""
    #     break main
    #     continue
    # """)
else:
    io = remote("{host}", {port})'''
    else:
        return f'''\
io = process("{binary}")
# Uncomment to attach GDB automatically:
# gdb.attach(io, gdbscript="""
#     break main
#     continue
# """)'''


# ─────────────────────────── Wizard / CLI logic ──────────────────────────────


def wizard() -> dict[str, str | bool]:
    """
    Run the interactive questionnaire and return a dictionary of answers.

    Returns:
        A dict with keys: ``binary``, ``host``, ``port``, ``has_win``,
        ``win_func``, ``has_libc``, ``libc``, ``output``.
    """
    print(f"\n{BOLD}{CYAN}🛠  CTF Pwntools Exploit Template Generator{RESET}\n")
    print(f"{YELLOW}Answer the questions below.  Press Enter to accept the default.{RESET}\n")

    binary  = _ask("Binary path (e.g. ./vuln)", "./vuln")
    remote_ = _ask("Remote host:port  (leave blank for local-only)", "")

    host, port = "", ""
    if remote_:
        # Accept both "host:port" and "host port" formats.
        parts = remote_.replace(":", " ").split()
        if len(parts) == 2:
            host, port = parts[0], parts[1]
        else:
            print(f"  {RED}[!] Could not parse host:port — defaulting to local only.{RESET}")

    has_win  = _ask_yes_no("Is there a known win / shell function?", False)
    win_func = ""
    if has_win:
        win_func = _ask("  Win function name", "win")

    has_libc = _ask_yes_no("Do you have a libc.so to use for ret2libc?", False)
    libc = ""
    if has_libc:
        libc = _ask("  Path to libc.so", "./libc.so.6")

    output = _ask("Output file name", "exploit.py")

    return {
        "binary":   binary,
        "host":     host,
        "port":     port,
        "has_win":  has_win,
        "win_func": win_func,
        "has_libc": has_libc,
        "libc":     libc,
        "output":   output,
    }


def generate(answers: dict[str, str | bool]) -> str:
    """
    Choose the right template builder based on the wizard's answers.

    Args:
        answers: Dictionary returned by :func:`wizard`.

    Returns:
        The complete exploit script as a string.
    """
    binary   = str(answers["binary"])
    host     = str(answers["host"])
    port     = str(answers["port"])
    has_win  = bool(answers["has_win"])
    win_func = str(answers["win_func"])
    has_libc = bool(answers["has_libc"])
    libc     = str(answers["libc"])

    if has_win:
        return _build_ret2win(binary, host, port, win_func)
    if has_libc:
        return _build_ret2libc(binary, host, port, libc)
    return _build_generic(binary, host, port)


def main() -> None:
    """Parse CLI flags, run the wizard (or use CLI values), write the output."""
    parser = argparse.ArgumentParser(
        description="Interactively generate a ready-to-run pwntools exploit template.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Interactive wizard (recommended for beginners):
  python3 pwn_template.py

  # Non-interactive — pass all values on the command line:
  python3 pwn_template.py --binary ./vuln --remote ctf.example.com:1337 \\
      --win-func win --output exploit.py
        """,
    )
    parser.add_argument("--binary",   default="",  help="Path to the binary.")
    parser.add_argument("--remote",   default="",  help="Remote host:port.")
    parser.add_argument("--win-func", default="",  help="Win function name.")
    parser.add_argument("--libc",     default="",  help="Path to libc.so.")
    parser.add_argument("--output",   default="",  help="Output file (default: exploit.py).")
    args = parser.parse_args()

    # If any CLI argument was given we skip the interactive wizard entirely.
    use_cli = any([args.binary, args.remote, args.win_func, args.libc])

    if use_cli:
        host, port = "", ""
        if args.remote:
            parts = args.remote.replace(":", " ").split()
            if len(parts) == 2:
                host, port = parts[0], parts[1]

        answers: dict[str, str | bool] = {
            "binary":   args.binary or "./vuln",
            "host":     host,
            "port":     port,
            "has_win":  bool(args.win_func),
            "win_func": args.win_func,
            "has_libc": bool(args.libc),
            "libc":     args.libc,
            "output":   args.output or "exploit.py",
        }
    else:
        answers = wizard()  # type: ignore[assignment]

    script = generate(answers)
    outfile = str(answers["output"]) or "exploit.py"

    out_path = Path(outfile)
    out_path.write_text(script, encoding="utf-8")
    # Make the output file executable so the user can run it directly.
    out_path.chmod(out_path.stat().st_mode | 0o755)

    print(f"\n{GREEN}{BOLD}[✓] Exploit template written to: {outfile}{RESET}")
    print(f"{YELLOW}    Next steps:")
    print(f"      1. Open {outfile} and fill in the TODO markers.")
    print(f"      2. python3 {outfile}{RESET}\n")


if __name__ == "__main__":
    main()
